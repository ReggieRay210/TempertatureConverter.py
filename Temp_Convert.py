fahrenheit= float(input('Enter the current temperature in °F:'))
convert_to_celsius = (fahrenheit-32)*(5/9)
print(fahrenheit, "°F is equal to", convert_to_celsius, "°C")



celsius = float(input('Enter the current temperature in °C:'))
convert_to_fahrenheit = (celsius*(9/5))+32
print(celsius, "°C is equal to", convert_to_fahrenheit, "°F")

